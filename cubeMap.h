void loadCubemap(GLuint& texture, vector<string> faces)
{
	glGenTextures(1, &texture);
	glBindTexture(GL_TEXTURE_CUBE_MAP, texture);

	bitmap_image image1(faces[0].c_str()); unsigned char* image_arr1= &image1.data_[0];
	bitmap_image image2(faces[1].c_str()); unsigned char* image_arr2 = &image2.data_[0];
	bitmap_image image3(faces[2].c_str()); unsigned char* image_arr3 = &image3.data_[0];
	bitmap_image image4(faces[3].c_str()); unsigned char* image_arr4 = &image4.data_[0];
	bitmap_image image5(faces[4].c_str()); unsigned char* image_arr5 = &image5.data_[0];
	bitmap_image image6(faces[5].c_str()); unsigned char* image_arr6 = &image6.data_[0]; 

	glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X, 0, GL_RGB, (int)image1.width_, (int)image1.height_, 0, GL_RGB, GL_UNSIGNED_BYTE, image_arr1);
	glTexImage2D(GL_TEXTURE_CUBE_MAP_NEGATIVE_X, 0, GL_RGB, (int)image2.width_, (int)image2.height_, 0, GL_RGB, GL_UNSIGNED_BYTE, image_arr2);
	glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_Y, 0, GL_RGB, (int)image3.width_, (int)image3.height_, 0, GL_RGB, GL_UNSIGNED_BYTE, image_arr3);
	glTexImage2D(GL_TEXTURE_CUBE_MAP_NEGATIVE_Y, 0, GL_RGB, (int)image4.width_, (int)image4.height_, 0, GL_RGB, GL_UNSIGNED_BYTE, image_arr4);
	glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_Z, 0, GL_RGB, (int)image5.width_, (int)image5.height_, 0, GL_RGB, GL_UNSIGNED_BYTE, image_arr5);
	glTexImage2D(GL_TEXTURE_CUBE_MAP_NEGATIVE_Z, 0, GL_RGB, (int)image6.width_, (int)image6.height_, 0, GL_RGB, GL_UNSIGNED_BYTE, image_arr6);

	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);
}

